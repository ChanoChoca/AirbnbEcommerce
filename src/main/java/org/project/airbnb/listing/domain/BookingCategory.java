package org.project.airbnb.listing.domain;

/**
 * Enum que representa las categorías de reserva para los listados de propiedades en la plataforma.
 */
public enum BookingCategory {
    // Categoría que representa todos los tipos de propiedades sin filtrado específico
    ALL,

    // Categoría para propiedades con vistas impresionantes
    AMAZING_VIEWS,

    // Categoría para propiedades únicas o inusuales como casas en árboles
    OMG, TREEHOUSES,

    // Categoría para propiedades cerca de la playa
    BEACH,

    // Categoría para propiedades en granjas
    FARMS,

    // Categoría para casas pequeñas, como casas diminutas
    TINY_HOMES,

    // Categoría para propiedades cerca de lagos
    LAKE, CONTAINERS,

    // Categoría para campamentos o vehículos recreativos
    CAMPING, CAMPERS,

    // Categoría para propiedades como castillos
    CASTLE, ARTIC, BOAT,

    // Categoría para alojamientos con desayuno incluido
    BED_AND_BREAKFASTS,

    // Categoría para habitaciones individuales
    ROOMS,

    // Categoría para propiedades construidas con materiales naturales o sostenibles
    EARTH_HOMES,

    // Categoría para propiedades en torres
    TOWER,

    // Categoría para propiedades en cuevas, lujosas, con cocina de chef
    CAVES, LUXES, CHEFS_KITCHEN,

    // Categoría para propiedades en áreas de esquí
    SKIING
}
